pub(crate) fn is_namespaced_r_call(name: &str) -> bool {
    let Some((pkg, sym)) = name.split_once("::") else {
        return false;
    };
    !pkg.is_empty() && !sym.is_empty() && !pkg.contains(':') && !sym.contains(':')
}

